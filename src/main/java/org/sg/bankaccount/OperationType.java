package org.sg.bankaccount;

/**
 * Enum representing the type of bank operation.
 * This enum is used to distinguish between different types of operations that can be performed on a bank account.
 *
 * The two supported operation types are:
 * - DEPOSIT: A deposit operation, where money is added to the account.
 * - WITHDRAWAL: A withdrawal operation, where money is subtracted from the account.
 */
public enum OperationType {
    DEPOSIT, WITHDRAWAL
}
